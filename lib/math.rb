def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  num1 - num2
  subtraction
end

def multiplication(num1, num2)
  num1 * num2
  multiplication
end

def division(num1, num2)
  num1 / num2
  division
end

def modulo(num1, num2)
  num1 % num2
  modulo
end

def square_root(num)
  num ** num
  square_root
end